void main(){
  // operator in dart
  // +, -, *, /, %, ++, --        Arithmetic operators
  // ==, !=, >, <, >=, <=    Comparison operators
  //&&, ||, !                         Logical operators
  // ? :                                Ternary  operator
  // condition ? do when true : do when false

  int aa = 10;

  print(aa < 99 ? "Hello" : "Hey");

  int bb = aa < 100 ? 20 : 5 * 10;
  print(bb);


}